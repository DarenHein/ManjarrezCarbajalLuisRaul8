// var es una variable que se puede cambair de tipo y valor 
var numero = 3 ;
// let es un tipo de variable que solo cambiara su valor 
let numero2 = 12 ; 
// cont es un tipo de variable que no cambiara su calor y tipo nunca 
const numero3 = 12 ; 

console.log(numero)
console.log(numero2)
console.log(numero3)

